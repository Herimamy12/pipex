#include "../include/libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dest;
	size_t	len;

	i = 0;
	len = ft_strlen(s);
	dest = NULL;
	dest = (char *)malloc((sizeof(char) * (len + 1)));
	if (dest == NULL)
		return (NULL);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
